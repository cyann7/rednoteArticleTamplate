如果团队每周都要产出访谈稿、案例稿、方法论文章或者产品解读，真正拖慢速度的通常不是“不会写”，而是资料总在不同地方、每次都从零开始、每个人的做法都不一样。于是同样一篇两千字内容，有时半天能出，有时改三轮还没法发。

这套流程是我们在内容团队里反复打磨出来的一个轻量版本，目标不是追求全自动，而是把那些反复出现、但不需要每次重新发明的动作稳定下来。它适合需要持续做长文、专题、案例总结的团队，也适合一个人单兵做内容但不想每次都被流程拖住的场景。

